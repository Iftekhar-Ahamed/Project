    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tbmodel = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            if (x) {
                new Passengerlist(tbmodel.getValueAt(jTable1.getSelectedRow(), 1).toString(), tbmodel.getValueAt(jTable1.getSelectedRow(), 2).toString()).setVisible(true);
                this.dispose();
            } else {
                new Ticket(tbmodel.getValueAt(jTable1.getSelectedRow(), 1).toString(), tbmodel.getValueAt(jTable1.getSelectedRow(), 2).toString()).setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

