    private void refreshtable() {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/btms", "root", "");
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement("SELECT * FROM `ticket`");

            ResultSet res = (ResultSet) stmt.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(res));

            conn.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void refreshtable(String s) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/btms", "root", "");
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement("SELECT * FROM `ticket` WHERE `DATE`=?");
            stmt.setString(1, s);

            ResultSet res = (ResultSet) stmt.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(res));

            conn.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

